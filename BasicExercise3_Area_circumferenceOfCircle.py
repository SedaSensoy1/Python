#Write a Python program to find the circumference and area of a circle with a given radius

radius= int(input("provide a radius of the circle to calculate the area and the circumference of the circle: "))

area= 3.14 * radius ** 2

circumference= 2 * 3.14 * radius

#print("The area of the circle is: ", area)
print(f"The area of the circle is: {area}") #You can also use this function

#print("The circumference of the circle is: ", circumference)
print(f"The circumference of the circle is: {circumference}") #You can also use this function