# Creating a tuple using parentheses
tuple_with_parentheses = (1, 2, 3)

# Creating a tuple without using parentheses
tuple_without_parentheses = 4, 5, 6

# If you're creating a tuple with only one element, you need to use a comma
single_element_tuple = 7,

# Printing the tuples
print(tuple_with_parentheses)    # (1, 2, 3)
print(tuple_without_parentheses) # (4, 5, 6)
print(single_element_tuple)      # (7,)
